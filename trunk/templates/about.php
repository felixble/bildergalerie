<h1>Vita</h1>

<p class="lead">
    Hildegard Blechschmitt, geboren in Speyer 1951, lebt in St. Ingbert und malt mit Liebe und Freude in ihrem kleinen
    Atelier, das schon aus den Nähten zu platzen drohte, wenn sich nicht durch glückliche Fügung die Chance aufgetan
    hätte, eine große Menge Bilder in den Fluren und Räumen einer Neunkircher Klinik  aufzuhängen.
</p>
<p class="lead">
    Die Lust am Malen packte sie schon zu Schulzeiten, wo eine einfühlsame  Lehrerin, die Malerin Elisabeth
    Mack-Usselmann in Speyer, ihre Neigung erkannte und unterstützte.
</p>
<p class="lead">
    Weitere Anregung und Fortbildung fand Hildegard Blechschmitt bei verschiedenen Dozenten zunächst im Bereich der
    Aquarellmalerei. Im Rahmen  von Sommerakademien in Blieskastel und St. Ingbert und diversen Workshops wandte sie
    sich immer mehr der Acrylmalerei zu, die ihr noch mehr die Möglichkeit bot, mit der Kraft der Farben
    pfälzisch-saarländische Lebensfreude auszudrücken.
</p>